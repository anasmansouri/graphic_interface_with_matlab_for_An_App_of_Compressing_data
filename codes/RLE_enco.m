function m_code=RLE_enco(m)
k=1;
j=1;
for i=1:(length(m)-1)
    if m(i)==m(i+1)
        k=k+1;
    else
        m_code(j)=k;
        m_code(j+1)=char(m(i));
        j=j+2;
        k=1;
    end
   
end
 m_code(j)=k;
 m_code(j+1)=char(m(length(m)));

     